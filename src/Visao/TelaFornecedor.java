    private void JTFornecedorMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_JTFornecedorMouseClicked
        if (JTFornecedor.getSelectedRow() != -1) {
            TxtCod.setText(JTFornecedor.getValueAt(JTFornecedor.getSelectedRow(), 0).toString());
            TxtNome.setText(JTFornecedor.getValueAt(JTFornecedor.getSelectedRow(), 1).toString());
            Txtcpf.setText(JTFornecedor.getValueAt(JTFornecedor.getSelectedRow(), 2).toString());
            TxtCNPJ.setText(JTFornecedor.getValueAt(JTFornecedor.getSelectedRow(), 3).toString());
            TxtTelefon.setText(JTFornecedor.getValueAt(JTFornecedor.getSelectedRow(), 4).toString());
            if (JTFornecedor.getValueAt(JTFornecedor.getSelectedRow(), 4).toString().equals("Física")) {
                jRFisica.setSelected(true);
            } else if (JTFornecedor.getValueAt(JTFornecedor.getSelectedRow(), 4).toString().equals("Jurídica")) {
                jRJurídica.setSelected(true);
            }
        }
    }//GEN-LAST:event_JTFornecedorMouseClicked

