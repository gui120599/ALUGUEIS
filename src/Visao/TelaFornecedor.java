    //carrega Tabela
    public void MostrarFornecedor() {
        JTFornecedor.getColumnModel().getColumn(0).setPreferredWidth(10);
        JTFornecedor.getColumnModel().getColumn(1).setPreferredWidth(200);
        JTFornecedor.getColumnModel().getColumn(2).setPreferredWidth(70);
        JTFornecedor.getColumnModel().getColumn(3).setPreferredWidth(70);
        JTFornecedor.getColumnModel().getColumn(4).setPreferredWidth(70);
        DefaultTableModel modelo = (DefaultTableModel) JTFornecedor.getModel();
        modelo.setNumRows(0);
        FornecedoresDao cdao = new FornecedoresDao();

        cdao.BuscarFornecedor().forEach((c) -> {
            modelo.addRow(new Object[]{
                c.getCod_Fornecedor(),
                c.getNome_Fornecedor(),
                c.getCPF_Fornecedor(),
                c.getCNPJ_Fornecedor(),
                c.getTipo_Pessoa()
            });
        });
    }

    //Limpar Campos
    public void LimparCampos() {
        TxtCod.setText("");
        TxtNome.setText("");
        Txtcpf.setText("");
        TxtCNPJ.setText("");
        TxtTelefon.setText("");
    }

    //Modifica para Fornecedor Físico
    public void PessoaFisica() {
        if (jRFisica.isSelected()) {
            TxtCNPJ.setEnabled(false);
            Txtcpf.setEnabled(true);
            TipoPessoa = "Física";
        } else if (jRJurídica.isSelected()) {
            TxtCNPJ.setEnabled(true);
            Txtcpf.setEnabled(false);
            TipoPessoa = "Jurídica";
        }
    }

    //Salva
    public void Salvar() {
        Fornecedores c = new Fornecedores();
        FornecedoresDao cdao = new FornecedoresDao();
        if (TxtNome.getText() == null || TxtNome.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Nome fornecedor é um campo Obrigatório!!");
            TxtNome.requestFocus();

        } else if (jRFisica.isSelected() && Txtcpf.getText() == null || Txtcpf.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "CPF é um campo Obrigatório!!");
            Txtcpf.requestFocus();

        } else if (jRJurídica.isSelected() && TxtCNPJ.getText() == null || TxtCNPJ.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "CNPJ é um campo obrigatório!!");
            TxtCNPJ.requestFocus();

        } else {

            c.setNome_Fornecedor(TxtNome.getText());
            c.setCPF_Fornecedor(Txtcpf.getText());
            c.setCNPJ_Fornecedor(TxtCNPJ.getText());
            c.setTelefone_Fornecedor(TxtTelefon.getText());
            c.setTipo_Pessoa(TipoPessoa);
            cdao.SalvarFornecedor(c);
            MostrarFornecedor();
            LimparCampos();
        }
    }

